/** Pads short listing blurbs so detail screens scroll (~10×). */
function expandDescription(lead: string, extras: string[]): string {
 const body = extras.join("\n\n");
 return `${lead}\n\n${body}\n\n${body}\n\n${lead}`;
}

/** Main stays first (feed card). Adds 5 extra Unsplash URLs for detail gallery. */
function images(main: string, seed: number): FeedItem["images"] {
 const extras = Array.from({ length: 5 }, (_, i) => {
 const url = EXTRA_PHOTOS[(seed * 5 + i) % EXTRA_PHOTOS.length];
 return { imageUrl: url, expiresAt: null };
 });
 return {
 mainImageUrl: { imageUrl: main, expiresAt: null },
 marketplaceImages: [{ imageUrl: main, expiresAt: null }, ...extras],
 imageUrlHostedByUs: main,
 };
}

